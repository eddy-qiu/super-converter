
public class Conversion {
	private String firstUnit;
	private String secondUnit;
	public void setFirstUnit(String first) {
		firstUnit = first;
	}
	public void setSecondUnit(String second) {
		secondUnit = second;
	}
	public String getFirstUnit() {
		return firstUnit;
	}
	public String getSecondUnit() {
		return secondUnit;
	}
}
